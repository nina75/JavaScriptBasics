//Write an expression that calculates trapezoid's area by given sides a and b and height h.

function calcArea(a, b, h) {
    return (a + b) * h / 2;
}

console.log(calcArea(5, 7, 12));
console.log(calcArea(2, 1, 33));
console.log(calcArea(8.5, 4.3, 2.7));
console.log(calcArea(100, 200, 300));
console.log(calcArea(0.222, 0.333, 0.555));